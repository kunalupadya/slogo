package GraphicsBackend;

/**
 * @author kunalupadya
 */
public class Point {
    private double myX;
    private double myY;

    public Point(double x, double y){
        myX = x;
        myY = y;
    }

    public double getMyX() {
        return myX;
    }

    public double getMyY() {
        return myY;
    }
}
